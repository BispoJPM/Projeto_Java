package dia04_repeticoes.exercicios;

public class Exercicio6 {
    public static void main(String[] args) {

        // Calcule a soma dos números de 1 a 100 usando for.

        int soma = 0;

        for (int a = 1; a <= 100; a++){
            soma += a;
            
        }
        System.out.println("A soma de 1 até 100 é: " + soma);
    
    }
}
